# 2. Objetivos e Público-Alvo

## 2.1 Objetivos do Curso
Proporcionar uma formação teórico-interdisciplinar em Psicoteologia, oferecendo uma compreensão ampliada e integral do ser humano. O curso busca promover o amadurecimento pessoal e intelectual dos estudantes, favorecendo uma visão de mundo crítica, coesa e transformadora.

### Objetivos Específicos
- Integrar saberes da mente humana, Filosofia e Teologia.
- Capacitar os estudantes para uma visão madura de si, do outro e do mundo.
- Estudar a relação entre razão, emoção, vontade e espiritualidade.
- Estimular a produção científica na área.

## 2.2 Público-Alvo
Psicólogos, profissionais da saúde mental e da saúde em geral, e indivíduos de diversas áreas que buscam aprofundar a compreensão do ser humano em suas múltiplas complexidades.

