# PPC: Pós-Graduação em Psicoteologia

Este documento contém todas as informações do Projeto Pedagógico do Curso (PPC) de Especialização em Psicoteologia da Faculdade Internacional Cidade Viva (FICV).

# 1. Apresentação e Identificação

A Pós-Graduação em Psicoteologia é um projeto que permite expandir e aprofundar o entendimento teórico sobre os aspectos teológicos integrados ao campo das ciências cognitivas, do comportamento e emoções com conhecimentos filosóficos. 

A Psicoteologia integra uma visão teológica, filosófica e antropológica sólida e integral do ser humano, que influencia profundamente os estudos sobre a alma humana. 

**Nota**: Trata-se de uma pós-graduação teórica, sem a pretensão de formação para uma prática clínica ou para atuação em psicoterapia.

